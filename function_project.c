#include <stdio.h>
#include <time.h>

//난이도 함수
int getRandomNumber(int level);

//문제 출제 함수
int showQuestion(int level, int num1, int num2);

//성공 함수
void success();

//실패 함수
void fail();

int main(void)
{
	//문이 5개가 있고, 각 문마다 점점 어려운 수식 퀴즈가 출제(랜덤)
	//맞히면 통과, 틀리면 실패

	srand(time(NULL)); //랜덤
	int count = 0;
	for (int i = 1; i <= 5; i++)
	{
		// x * y = ?
		int num1 = getRandomNumber(i); 
		int num2 = getRandomNumber(i);

		//printf("%d X %d =?", num1, num2)
		showQuestion(i, num1, num2);

		int answer = -1;
		scanf_s("%d", &answer);
		if (answer == -1)
		{
			printf("프로그램을 종료합니다.");
			exit(0);
		}
		else if (answer == num1 * num2)
		{
			//성공
			success();
			count++;

		}
		else
		{

			//실패
			fail();
		}
	}

	printf("\n\n 당신은 비밀번호 5개 중에서 %d개를 맞췄습니다", count);

	return 0;
}

int getRandomNumber(int level)
{
	//level 당 단어가 7개씩 증가하도록 난이도 설정
	return rand() % (level * 7) + 1;
}

int showQuestion(int level, int num1, int num2)
{

	printf("\n\n\n########## %d 번째 비밀번호 #########\n", level);
	printf("\n\t%d X %d는?\n\n", num1, num2);
	printf("####################################\n");
	printf("\n비밀번호를 입력하세요 (종료 : -1 >>");


}

void success()
{
	printf("성공!");
	
}

void fail()
{
	printf("실패!");

}